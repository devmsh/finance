    /**
     * Handle the user "created" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function created(User $user)
    {
        Category::defaultCategories()->each(function ($category) use ($user) {
            Category::create(array_merge($category->toArray(), [
                'id' => null,
                'user_id' => $user->id,
                'source_id' => $category->id,
            ]));
        });
    }

